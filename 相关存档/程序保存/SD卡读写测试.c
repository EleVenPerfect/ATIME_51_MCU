void serial_int(void)
{
	PCON &= 0x7f;	//波特率不倍速
	SCON = 0x50;	//8位数据,可变波特率
	TMOD &= 0x0f;	//清除定时器1模式位
	TMOD |= 0x20;	//设定定时器1为8位自动重装方式
	TL1 = 0xFD;		//设定定时初值
	TH1 = 0xFD;		//设定定时器重装值
	ET1 = 0;		//禁止定时器1中断
	TR1 = 1;		//启动定时器1
}


void send_char(unsigned char dat)
{
   SBUF=dat;
   while(!TI);
   TI=0;
}

